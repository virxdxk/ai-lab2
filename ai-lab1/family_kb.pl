% ---------- Правила (7 шт.) ----------
% 1) отец: X — отец Y, если X родитель Y и X — мужчина
father(X,Y) :- parent(X,Y), male(X).

% 2) мать: X — мать Y, если X родитель Y и X — женщина
mother(X,Y) :- parent(X,Y), female(X).

% 3) брат/сестра: X и Y — сиблинг, если у них есть общий родитель (и X \= Y)
sibling(X,Y) :-
    parent(P,X),
    parent(P,Y),
    X \= Y.

% 4) брат: X — брат Y, если X сиблинг Y и X — мужчина
brother(X,Y) :- sibling(X,Y), male(X).

% 5) сестра: X — сестра Y, если X сиблинг Y и X — женщина
sister(X,Y) :- sibling(X,Y), female(X).

% 6) бабушка/дедушка: G — дед/бабка C, если G — родитель P, а P — родитель C
grandparent(G,C) :-
    parent(G,P),
    parent(P,C).

% 7) тётя: A — тётя N, если A — женщина и A — сиблинг одного из родителей N
aunt(A,N) :-
    female(A),
    parent(P,N),
    sibling(A,P).
